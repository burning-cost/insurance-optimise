# ---------------------------------------------------------------------------
# Claim size helper
# ---------------------------------------------------------------------------


def _draw_claim(
    b: float,
    dist: str,
    params: dict,
    rng: np.random.Generator,
) -> float:
    """Draw a single claim size with mean b from the given distribution."""
    if dist == "exponential":
        return float(rng.exponential(b))
    elif dist == "lognormal":
        sigma = float(params.get("sigma", 1.0))
        # Lognormal with E[Y] = b: mu_ln = log(b) - sigma^2/2
        mu_ln = np.log(b) - 0.5 * sigma ** 2
        return float(rng.lognormal(mean=mu_ln, sigma=sigma))
    elif dist == "gamma":
        shape = float(params.get("shape", 1.0))
        scale = b / shape
        return float(rng.gamma(shape=shape, scale=scale))
    else:
        raise ValueError(
            f"Unknown claim distribution {dist!r}. "
            "Use 'exponential', 'lognormal', or 'gamma'."
        )
