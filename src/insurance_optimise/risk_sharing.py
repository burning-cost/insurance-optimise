"""
LinearRiskSharingPool: community-based insurance with allocation matrix.

Implements the linear risk-sharing framework from Denuit, Flores-Contró, and
Robert (2026), arXiv:2603.29530, "Linear Risk Sharing in Community-Based
Insurance."

The model
---------
Each participant i has a Cramér-Lundberg surplus process:

    U_i(t) = u_i + c_i * t - S_i(t)

where c_i = (1 + eta_i) * lambda_i * b_i is the premium rate, lambda_i is
claim intensity, b_i = E[Y_{i,1}] is mean claim size, and eta_i is the safety
loading. S_i(t) is a compound Poisson process with rate lambda_i.

Under the linear sharing rule with allocation matrix A = (a_{i,j}), participant
i absorbs fraction a_{i,j} of each claim generated by participant j. The pooled
surplus for participant i becomes:

    U_i^pool(t) = u_i + c_i * t - sum_j sum_{k: claim k from j} a_{i,j} * Y_{j,k}

Three matrix conditions are necessary (and sufficient under the scale family
assumption) to guarantee that pooling weakly reduces ruin probability for every
participant:

1. BUDGET BALANCE: sum_i a_{i,j} = 1 for all j (columns sum to 1)
2. ACTUARIAL FAIRNESS: lambda_i * b_i = sum_j a_{i,j} * lambda_j * b_j for all i
   (expected claim income unchanged per participant)
3. CAPACITY: a_{i,j} * b_j <= b_i for all i, j
   (no single transferred claim exceeds recipient capacity)

IMPORTANT: The theoretical guarantee (Proposition 3.8) requires a fourth
condition that cannot be checked by this class at construction time:
SCALE FAMILY — claim sizes must satisfy Y_{i,k} =^d b_i * W where W is a
common non-negative random variable with E[W] = 1. Exponential, Gamma with
fixed shape, and Lognormal with fixed sigma all satisfy this. Pareto does not
(unless normalised). Mixed severities do not. If your claim size distribution
does not belong to the same scale family across participants, the ruin
improvement guarantee does NOT hold.

IMPORTANT: The exact Cramér-Lundberg ruin formula (method='cramerlundberg')
is correct only for exponential severity. For any other distribution you must
use method='simulation'. Passing a non-exponential claim_dist with
method='cramerlundberg' raises ValueError.

Usage
-----
>>> import numpy as np
>>> from insurance_optimise import LinearRiskSharingPool

>>> pool = LinearRiskSharingPool.mean_proportional(
...     claim_intensities=np.array([2.0, 1.0, 3.0]),
...     claim_means=np.array([2.0, 0.5, 1.0]),
...     safety_loadings=np.array([0.4, 0.4, 0.4]),
... )
>>> result = pool.validate_conditions()
>>> print(result)
>>> ruin = pool.ruin_comparison()
>>> print(ruin.pooled)      # pooled ruin probabilities
>>> print(ruin.standalone)  # standalone ruin probabilities
>>> print(ruin.improvement) # positive means pooling helped

>>> sim = pool.simulate(T=50.0, n_paths=1000, seed=42)
>>> print(sim.empirical_ruin_probability)

>>> optimal = pool.optimal_allocation(objective='min_max_ruin')
>>> print(optimal.audit_trail())

References
----------
Denuit, M., Flores-Contró, J.M., Robert, C.Y. (2026). "Linear Risk Sharing
in Community-Based Insurance." arXiv:2603.29530.
"""

