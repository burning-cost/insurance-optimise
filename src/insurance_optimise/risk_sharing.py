def _cl_ruin_exponential(
    lam: float,
    b: float,
    c: float,
    u: float,
) -> float:
    """
    Exact infinite-time ruin probability for a Cramér-Lundberg process
    with exponential claim sizes.

    The net profit condition requires c > lambda * b, i.e. safety loading
    eta > 0.

    Formula (Pollaczek-Khinchine for exponential):
        psi(u) = (lambda * b / c) * exp(-(c/b - lambda) * u / b)
              = rho * exp(-R * u)
    where rho = lambda * b / c and R = (c/b - lambda) / b = c/b^2 - lambda/b.

    For u=0:  psi(0) = lambda * b / c = 1 / (1 + eta).

    Parameters
    ----------
    lam : claim intensity (events per year)
    b   : mean claim size
    c   : premium rate (must satisfy c > lambda * b)
    u   : initial capital (must be >= 0)

    Returns
    -------
    float in [0, 1].
    """
    if c <= lam * b:
        # Net profit condition violated — certain ruin
        return 1.0
    rho = (lam * b) / c  # psi(0), in (0,1) when NPC holds
    R = (c - lam * b) / (b * c)  # Lundberg exponent  > 0
    return float(rho * np.exp(-R * u))


def _effective_pooled_process(
    i: int,
    allocation_matrix: np.ndarray,
    claim_intensities: np.ndarray,
    claim_means: np.ndarray,
    safety_loadings: np.ndarray,
) -> tuple[float, float, float]:
    """
    Compute effective (lambda_eff, b_eff, c_i) for participant i's pooled surplus.

    Under the scale family assumption (Y_{j,k} =^d b_j * W) with exponential W,
    participant i absorbs claims from all participants j weighted by a_{i,j}.
    The resulting claim process for i is a superposition of Poisson processes
    with intensities lambda_j and claim sizes a_{i,j} * Y_j. Each component
    a_{i,j} * Y_j is exponential with mean a_{i,j} * b_j.

    For a superposition of independent compound Poisson processes with
    exponential claims of possibly different means, the aggregate is NOT
    exponential in general. However, if all means happen to be equal, it is.

    This is the key restriction: the Cramér-Lundberg formula here applies
    under the HOMOGENEOUS SCALE FAMILY assumption where all a_{i,j} * b_j / b_i
    have the same scale (equivalently, the a_{i,j} are proportional to b_i / b_j).
    In practice, for the mean-proportional rule, this is approximately satisfied.

    We use the AGGREGATE first-two-moments approach: treat the pooled claim
    process as exponential with the same mean (this is exact for homogeneous
    exponential scale family, approximate otherwise). The user is warned via
    the docstring.

    Returns (lambda_eff, b_eff, c_i) where:
      lambda_eff = total rate of claims hitting participant i
      b_eff      = effective mean claim size per event
      c_i        = premium rate for participant i
    """
    n = len(claim_intensities)
    # Total expected claims per unit time hitting i
    expected_claims_i = float(
        np.sum(allocation_matrix[i, :] * claim_intensities * claim_means)
    )
    # Total event rate hitting i: only count sources where a_{i,j} > 0
    # (zero-allocation sources contribute nothing to i's surplus)
    active = allocation_matrix[i, :] > 1e-15
    lam_eff = float(np.sum(claim_intensities[active]))
    # Effective mean per event
    if lam_eff > 1e-15:
        b_eff = expected_claims_i / lam_eff
    else:
        b_eff = 0.0
    c_i = float(
        (1.0 + safety_loadings[i]) * claim_intensities[i] * claim_means[i]
    )
    return lam_eff, b_eff, c_i


