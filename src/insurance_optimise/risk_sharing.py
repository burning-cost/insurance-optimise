class LinearRiskSharingPool:
    """
    Linear risk-sharing pool for community-based insurance.

    Models n participants each with a Cramér-Lundberg surplus process.
    Claim costs are redistributed at claim time via a pre-specified n×n
    allocation matrix A = (a_{i,j}), where participant i absorbs fraction
    a_{i,j} of each claim generated by participant j.

    Reference: Denuit, Flores-Contró, Robert (2026), arXiv:2603.29530.

    SCALE FAMILY WARNING
    --------------------
    The theoretical guarantee that pooling reduces ruin probability
    (Proposition 3.8 of the paper) requires claim sizes to belong to a
    common scale family: Y_{i,k} =^d b_i * W for a shared non-negative W
    with E[W]=1. Exponential (shape=1 Gamma), Gamma with fixed shape, and
    Lognormal with fixed sigma all satisfy this. Pareto does not (in general).
    Mixed severity distributions across participants do not.

    If your data does not satisfy the scale family condition, the analytical
    ruin reduction guarantee does not apply. Use simulate() to check
    empirically.

    EXACT RUIN FORMULA
    ------------------
    The Cramér-Lundberg ruin probability formula (method='cramerlundberg')
    is exact only for exponential claim sizes. For any other distribution,
    use method='simulation'.
    """

    def __init__(
        self,
        allocation_matrix: np.ndarray,
        claim_intensities: np.ndarray,
        claim_means: np.ndarray,
        safety_loadings: np.ndarray,
        initial_capital: np.ndarray | None = None,
    ) -> None:
        """
        Construct a LinearRiskSharingPool directly from an allocation matrix.

        Parameters
        ----------
        allocation_matrix:
            (n, n) ndarray. Entry (i, j) is the fraction of participant j's
            claims absorbed by participant i. Columns should sum to 1
            (budget balance). Rows encode each participant's sharing rule.
        claim_intensities:
            Shape (n,). Poisson claim arrival rate lambda_i for each
            participant (claims per year).
        claim_means:
            Shape (n,). Mean claim size b_i = E[Y_{i,1}] for each participant.
        safety_loadings:
            Shape (n,). Safety loading eta_i for each participant. Premium
            rate is c_i = (1 + eta_i) * lambda_i * b_i. Must be > 0.
        initial_capital:
            Shape (n,). Initial surplus u_i for each participant. If None,
            defaults to 1.0 for all participants.

        Raises
        ------
        ValueError
            If any input has incompatible shape, or any safety loading is
            non-positive, or any intensity/mean is non-positive.
        """
        allocation_matrix = np.asarray(allocation_matrix, dtype=float)
        claim_intensities = np.asarray(claim_intensities, dtype=float)
        claim_means = np.asarray(claim_means, dtype=float)
        safety_loadings = np.asarray(safety_loadings, dtype=float)

        n = len(claim_intensities)

        if allocation_matrix.shape != (n, n):
            raise ValueError(
                f"allocation_matrix must be ({n}, {n}), "
                f"got {allocation_matrix.shape}"
            )
        if claim_means.shape != (n,):
            raise ValueError(f"claim_means must have shape ({n},), got {claim_means.shape}")
        if safety_loadings.shape != (n,):
            raise ValueError(f"safety_loadings must have shape ({n},), got {safety_loadings.shape}")
        if np.any(claim_intensities <= 0):
            raise ValueError("All claim_intensities must be positive.")
        if np.any(claim_means <= 0):
            raise ValueError("All claim_means must be positive.")
        if np.any(safety_loadings <= 0):
            raise ValueError(
                "All safety_loadings must be positive (eta_i > 0 is required "
                "for the net profit condition and finite ruin probability)."
            )
        if np.any(allocation_matrix < 0):
            raise ValueError("All allocation_matrix entries must be non-negative.")

        if initial_capital is None:
            initial_capital = np.ones(n)
        else:
            initial_capital = np.asarray(initial_capital, dtype=float)
            if initial_capital.shape != (n,):
                raise ValueError(
                    f"initial_capital must have shape ({n},), "
                    f"got {initial_capital.shape}"
                )
            if np.any(initial_capital < 0):
                raise ValueError("All initial_capital values must be non-negative.")

        self._A = allocation_matrix
        self._lambda = claim_intensities
        self._b = claim_means
        self._eta = safety_loadings
        self._u = initial_capital
        self._n = n

    @classmethod
    def mean_proportional(
        cls,
        claim_intensities: np.ndarray,
        claim_means: np.ndarray,
        safety_loadings: np.ndarray,
        initial_capital: np.ndarray | None = None,
    ) -> "LinearRiskSharingPool":
        """
        Construct a pool using the mean-proportional allocation rule.

        Under the mean-proportional rule, participant i's share of any
        participant j's claim is proportional to i's expected claim volume:

            a_{i,j}^np = lambda_i * b_i / sum_k (lambda_k * b_k)

        This rule gives every participant the same fraction of every loss,
        regardless of the loss source. It satisfies budget balance and
        actuarial fairness by construction. Capacity is not guaranteed —
        check with validate_conditions().

        The mean-proportional rule is the simplest and most auditable choice:
        allocation fractions are determined entirely by each participant's
        expected annual claim cost, which is easily observable and defensible
        to regulators.

        Parameters
        ----------
        claim_intensities:
            Shape (n,). Claim arrival rates.
        claim_means:
            Shape (n,). Mean claim sizes.
        safety_loadings:
            Shape (n,). Safety loadings eta_i > 0.
        initial_capital:
            Shape (n,) or None. If None, defaults to 1.0 per participant.

        Returns
        -------
        LinearRiskSharingPool
        """
        claim_intensities = np.asarray(claim_intensities, dtype=float)
        claim_means = np.asarray(claim_means, dtype=float)
        if np.any(claim_intensities <= 0):
            raise ValueError("All claim_intensities must be positive.")
        if np.any(claim_means <= 0):
            raise ValueError("All claim_means must be positive.")
        n = len(claim_intensities)
        volumes = claim_intensities * claim_means  # lambda_i * b_i
        total_volume = np.sum(volumes)
        if total_volume <= 0:
            raise ValueError("Total expected claim volume (sum lambda_i * b_i) must be positive.")
        shares = volumes / total_volume  # shape (n,)
        # Every column is the same (shares vector)
        A = np.outer(shares, np.ones(n))  # A[i,j] = shares[i] for all j
        return cls(
            allocation_matrix=A,
            claim_intensities=claim_intensities,
            claim_means=claim_means,
            safety_loadings=safety_loadings,
            initial_capital=initial_capital,
        )

    # ------------------------------------------------------------------
    # Properties
    # ------------------------------------------------------------------

    @property
    def n_participants(self) -> int:
        """Number of participants in the pool."""
        return self._n

    @property
    def allocation_matrix(self) -> np.ndarray:
        """The (n, n) allocation matrix A."""
        return self._A.copy()

    @property
    def claim_intensities(self) -> np.ndarray:
        """Claim intensities lambda_i, shape (n,)."""
        return self._lambda.copy()

    @property
    def claim_means(self) -> np.ndarray:
        """Mean claim sizes b_i, shape (n,)."""
        return self._b.copy()

    @property
    def safety_loadings(self) -> np.ndarray:
        """Safety loadings eta_i, shape (n,)."""
        return self._eta.copy()

    @property
    def initial_capital(self) -> np.ndarray:
        """Initial surplus u_i, shape (n,)."""
        return self._u.copy()

    @property
    def premium_rates(self) -> np.ndarray:
        """Premium rates c_i = (1 + eta_i) * lambda_i * b_i, shape (n,)."""
        return (1.0 + self._eta) * self._lambda * self._b

    # ------------------------------------------------------------------
    # Validation
    # ------------------------------------------------------------------

    def validate_conditions(self, tol: float = 1e-8) -> ValidationResult:
        """
        Check the three matrix conditions for ruin probability improvement.

        Checks:
          1. Budget balance: sum_i a_{i,j} = 1 for all j
          2. Actuarial fairness: lambda_i * b_i = sum_j a_{i,j} * lambda_j * b_j
          3. Capacity: a_{i,j} * b_j <= b_i for all i, j

        Does NOT check the scale family condition (distributional assumption).

        Parameters
        ----------
        tol:
            Absolute tolerance for budget balance and capacity checks.
            For actuarial fairness, relative tolerance (normalised by
            lambda_i * b_i).

        Returns
        -------
        ValidationResult
        """
        A = self._A
        lam = self._lambda
        b = self._b

        # 1. Budget balance: column sums
        col_sums = A.sum(axis=0)  # shape (n,)
        bb_violations = col_sums - 1.0
        budget_balance_ok = bool(np.all(np.abs(bb_violations) <= tol))

        # 2. Actuarial fairness
        # LHS: lambda_i * b_i for each i
        lhs = lam * b  # shape (n,)
        # RHS: sum_j a_{i,j} * lambda_j * b_j for each i
        rhs = A @ (lam * b)  # shape (n,)
        # Relative violations
        denominator = np.maximum(lhs, 1e-15)
        af_violations = (lhs - rhs) / denominator
        actuarial_fairness_ok = bool(np.all(np.abs(af_violations) <= tol))

        # 3. Capacity: a_{i,j} * b_j <= b_i
        # For each (i, j): A[i,j] * b[j] - b[i]
        # capacity_matrix[i,j] = A[i,j] * b[j] - b[i]
        capacity_matrix = A * b[np.newaxis, :] - b[:, np.newaxis]
        max_cap_violation = float(np.max(capacity_matrix))
        capacity_ok = bool(max_cap_violation <= tol)

        return ValidationResult(
            budget_balance_ok=budget_balance_ok,
            actuarial_fairness_ok=actuarial_fairness_ok,
            capacity_ok=capacity_ok,
            budget_balance_violations=bb_violations,
            actuarial_fairness_violations=af_violations,
            capacity_violations=max_cap_violation,
        )

    # ------------------------------------------------------------------
    # Ruin probabilities
    # ------------------------------------------------------------------

    def _ruin_probability_single(
        self,
        lam_eff: float,
        b_eff: float,
        c: float,
        u: float,
        method: str,
        n_sim: int,
        T: float | None,
        claim_dist: str,
        claim_dist_params: dict | None,
        rng: np.random.Generator,
    ) -> float:
        """Internal: compute ruin probability for one participant."""
        if method == "cramerlundberg":
            if claim_dist != "exponential":
                raise ValueError(
                    "method='cramerlundberg' is exact only for exponential severity. "
                    "For non-exponential distributions, use method='simulation'."
                )
            return _cl_ruin_exponential(lam_eff, b_eff, c, u)
        elif method == "simulation":
            return self._simulate_ruin_single(
                lam_eff, b_eff, c, u, n_sim, T, claim_dist, claim_dist_params, rng
            )
        else:
            raise ValueError(f"Unknown method {method!r}. Use 'cramerlundberg' or 'simulation'.")

    def _simulate_ruin_single(
        self,
        lam: float,
        b: float,
        c: float,
        u: float,
        n_sim: int,
        T: float | None,
        claim_dist: str,
        claim_dist_params: dict | None,
        rng: np.random.Generator,
    ) -> float:
        """Simulate ruin probability for a single surplus process."""
        if T is None:
            T = 200.0  # approximate infinite-time via long horizon
        params = claim_dist_params or {}
        ruin_count = 0
        for _ in range(n_sim):
            surplus = u
            t = 0.0
            ruined = False
            while t < T:
                dt_next = rng.exponential(1.0 / lam)
                t_next = t + dt_next
                if t_next > T:
                    break
                # Premium income up to next claim
                surplus += c * dt_next
                # Claim size
                claim = _draw_claim(b, claim_dist, params, rng)
                surplus -= claim
                t = t_next
                if surplus < 0:
                    ruined = True
                    break
            if ruined:
                ruin_count += 1
        return ruin_count / n_sim

    def ruin_comparison(
        self,
        method: str = "cramerlundberg",
        n_sim: int = 10_000,
        time_horizon: float | None = None,
        claim_dist: str = "exponential",
        claim_dist_params: dict | None = None,
        seed: int = 42,
    ) -> RuinResult:
        """
        Compare pooled vs standalone ruin probabilities for all participants.

        Exact for exponential severity (method='cramerlundberg'). For other
        distributions, use method='simulation'.

        Parameters
        ----------
        method:
            'cramerlundberg' — exact Pollaczek-Khinchine formula; requires
            claim_dist='exponential'.
            'simulation' — Monte Carlo estimate.
        n_sim:
            Number of simulation paths per participant. Used only for
            method='simulation'.
        time_horizon:
            Simulation horizon T in years. None means infinite-time
            approximation (T=200). Only used for method='simulation'.
        claim_dist:
            'exponential' or 'lognormal'. Only 'exponential' supports
            method='cramerlundberg'.
        claim_dist_params:
            Extra parameters for the claim distribution. For 'lognormal',
            pass {'sigma': value} (sigma of the log-normal, not the mean).
        seed:
            Random seed for reproducibility.

        Returns
        -------
        RuinResult
            .pooled — ruin probabilities under the pool (shape n)
            .standalone — ruin probabilities without pooling (shape n)
            .improvement — standalone - pooled (positive = pool helped)
            .method — 'cramerlundberg' or 'simulation'
        """
        if method == "cramerlundberg" and claim_dist != "exponential":
            raise ValueError(
                "method='cramerlundberg' requires claim_dist='exponential'. "
                "Use method='simulation' for other distributions."
            )

        rng = np.random.default_rng(seed)

        pooled_probs = np.empty(self._n)
        standalone_probs = np.empty(self._n)

        for i in range(self._n):
            # --- Standalone ---
            c_i = float((1.0 + self._eta[i]) * self._lambda[i] * self._b[i])
            standalone_probs[i] = self._ruin_probability_single(
                lam_eff=float(self._lambda[i]),
                b_eff=float(self._b[i]),
                c=c_i,
                u=float(self._u[i]),
                method=method,
                n_sim=n_sim,
                T=time_horizon,
                claim_dist=claim_dist,
                claim_dist_params=claim_dist_params,
                rng=rng,
            )

            # --- Pooled ---
            # Under scale family (exponential): the pooled process for
            # participant i is a superposition of compound Poisson processes.
            # Each participant j generates events at rate lambda_j; i absorbs
            # fraction a_{i,j} of each, giving effective claim size a_{i,j}*Y_j.
            # For method='cramerlundberg', we aggregate into a single effective
            # process using the aggregate mean (exact for homogeneous exponential
            # scale family by the memoryless / convolution property of exponential).
            lam_eff, b_eff, c_i = _effective_pooled_process(
                i, self._A, self._lambda, self._b, self._eta
            )
            pooled_probs[i] = self._ruin_probability_single(
                lam_eff=lam_eff,
                b_eff=b_eff,
                c=c_i,
                u=float(self._u[i]),
                method=method,
                n_sim=n_sim,
                T=time_horizon,
                claim_dist=claim_dist,
                claim_dist_params=claim_dist_params,
                rng=rng,
            )

        improvement = standalone_probs - pooled_probs

        return RuinResult(
            pooled=pooled_probs,
            standalone=standalone_probs,
            improvement=improvement,
            method=method,
            initial_capital=self._u.copy(),
        )

    # ------------------------------------------------------------------
    # Event-driven simulation
    # ------------------------------------------------------------------

    def simulate(
        self,
        T: float = 50.0,
        n_paths: int = 1000,
        claim_dist: str = "exponential",
        claim_dist_params: dict | None = None,
        seed: int = 42,
    ) -> SimulationResult:
        """
        Event-driven Monte Carlo simulation of the pool.

        Uses a Gillespie-style event-driven approach: inter-arrival times
        are drawn from Exponential(sum lambda_j), then the event is assigned
        to participant j with probability lambda_j / sum_k lambda_k. This is
        more efficient than per-participant simulation when n is small relative
        to expected event count.

        At each event for participant j, claim size Y_j is drawn and added
        to participant i's losses as a_{i,j} * Y_j.

        Parameters
        ----------
        T:
            Simulation horizon in years.
        n_paths:
            Number of independent Monte Carlo paths.
        claim_dist:
            'exponential' or 'lognormal'. Claim size distribution.
            All participants use the same family (scale family assumption).
        claim_dist_params:
            Extra distribution parameters. For 'lognormal', pass
            {'sigma': value}. If None, uses default sigma=1.0 for lognormal.
        seed:
            Random seed.

        Returns
        -------
        SimulationResult

        Warns
        -----
        PerformanceWarning
            When n_participants * n_paths * lambda_total * T > 1e7. The
            computation will still proceed but may be slow.
        """
        params = claim_dist_params or {}
        lam_total = float(np.sum(self._lambda))
        expected_events = n_paths * lam_total * T
        complexity = self._n * expected_events

        if complexity > 1e7:
            warnings.warn(
                f"High complexity: n_participants={self._n} * n_paths={n_paths} * "
                f"lambda_total={lam_total:.1f} * T={T} ≈ {complexity:.2e} operations. "
                "Simulation may be slow. Consider reducing n_paths or T.",
                PerformanceWarning,
                stacklevel=2,
            )

        rng = np.random.default_rng(seed)
        c = self.premium_rates  # shape (n,)

        # Accumulate results across paths
        ruin_flags = np.zeros((n_paths, self._n), dtype=bool)
        ruin_times_sum = np.zeros(self._n)

        # Pre-compute claim distribution probabilities for event assignment
        lam_probs = self._lambda / lam_total  # shape (n,), normalised

        for path in range(n_paths):
            surplus = self._u.copy()
            t = 0.0

            while t < T:
                # Time to next event from any participant
                dt = rng.exponential(1.0 / lam_total)
                t_next = t + dt
                if t_next > T:
                    # Accrue remaining premium, no more events
                    surplus += c * (T - t)
                    break

                # Premium income during dt
                surplus += c * dt
                t = t_next

                # Which participant generated this claim?
                j = int(rng.choice(self._n, p=lam_probs))

                # Draw claim size for participant j
                Y_j = _draw_claim(float(self._b[j]), claim_dist, params, rng)

                # Apply sharing: each participant i absorbs a_{i,j} * Y_j
                surplus -= self._A[:, j] * Y_j

                # Check for ruin
                ruined = surplus < 0.0
                if np.any(ruined) and not np.all(ruin_flags[path]):
                    newly_ruined = ruined & ~ruin_flags[path]
                    ruin_flags[path] |= newly_ruined
                    ruin_times_sum[newly_ruined] += t

                # If all participants ruined, stop early
                if np.all(ruin_flags[path]):
                    break

        ruin_count = ruin_flags.sum(axis=0)  # shape (n,)
        empirical_prob = ruin_count / n_paths

        with np.errstate(invalid="ignore"):
            mean_ruin_time = np.where(
                ruin_count > 0,
                ruin_times_sum / np.maximum(ruin_count, 1),
                np.nan,
            )

        return SimulationResult(
            empirical_ruin_probability=empirical_prob,
            mean_ruin_time=mean_ruin_time,
            ruin_count=ruin_count.astype(int),
            n_paths=n_paths,
            T=T,
            claim_dist=claim_dist,
            n_participants=self._n,
        )

    # ------------------------------------------------------------------
    # Optimal allocation
    # ------------------------------------------------------------------

    def optimal_allocation(
        self,
        objective: str = "min_max_ruin",
        capacity_slack: float = 0.0,
        fairness_tol: float = 1e-8,
    ) -> "LinearRiskSharingPool":
        """
        Optimise the allocation matrix via SLSQP.

        Finds the allocation matrix A that minimises (or maximises) the
        chosen objective, subject to budget balance and actuarial fairness
        as equality constraints, and capacity as inequality constraints.

        Parameters
        ----------
        objective:
            'min_max_ruin':
                Minimise the maximum ruin probability across participants
                (min-max criterion). Protects the worst-off participant.
            'max_min_improvement':
                Maximise the minimum ruin improvement across participants.
                Equivalent to min_max_ruin under the scale family.
        capacity_slack:
            Additional slack in capacity constraint. Setting > 0 relaxes
            capacity: a_{i,j} * b_j <= b_i + capacity_slack * b_j.
        fairness_tol:
            Tolerance for equality constraints in the SLSQP solver.

        Returns
        -------
        LinearRiskSharingPool
            A new pool instance with the optimised allocation matrix. The
            original pool is unchanged.

        Warns
        -----
        PerformanceWarning
            When n_participants > 30. SLSQP with n^2 free variables becomes
            slow for large n.

        Notes
        -----
        The optimisation treats all n^2 entries of A as free variables (not
        just the upper triangle), which allows asymmetric solutions. Budget
        balance is enforced as n equality constraints (column sums = 1).
        Actuarial fairness is enforced as n additional equality constraints.
        Capacity is enforced as n^2 inequality constraints. Non-negativity is
        enforced as bounds.
        """
        if self._n > 30:
            warnings.warn(
                f"optimal_allocation() with n_participants={self._n} is slow. "
                "SLSQP has n^2 variables. Consider n <= 30.",
                PerformanceWarning,
                stacklevel=2,
            )

        n = self._n
        lam = self._lambda
        b = self._b
        eta = self._eta

        # Variables: x = A.flatten(), shape (n*n,)
        x0 = self._A.flatten()

        def unpack(x: np.ndarray) -> np.ndarray:
            return x.reshape(n, n)

        def _ruin_probs(A: np.ndarray) -> np.ndarray:
            """Compute ruin probability for each participant given A."""
            probs = np.empty(n)
            for i in range(n):
                lam_eff_i = float(np.sum(lam))
                b_eff_i_num = float(np.dot(A[i, :], lam * b))
                b_eff_i = b_eff_i_num / lam_eff_i if lam_eff_i > 0 else 0.0
                c_i = float((1.0 + eta[i]) * lam[i] * b[i])
                probs[i] = _cl_ruin_exponential(lam_eff_i, b_eff_i, c_i, float(self._u[i]))
            return probs

        if objective == "min_max_ruin":
            def obj_fn(x: np.ndarray) -> float:
                A = unpack(x)
                return float(np.max(_ruin_probs(A)))
        elif objective == "max_min_improvement":
            # standalone probs are fixed
            standalone = np.array([
                _cl_ruin_exponential(
                    float(lam[i]),
                    float(b[i]),
                    float((1.0 + eta[i]) * lam[i] * b[i]),
                    float(self._u[i]),
                )
                for i in range(n)
            ])

            def obj_fn(x: np.ndarray) -> float:
                A = unpack(x)
                improvement = standalone - _ruin_probs(A)
                # Maximise min improvement = minimise -min improvement
                return float(-np.min(improvement))
        else:
            raise ValueError(
                f"Unknown objective {objective!r}. "
                "Use 'min_max_ruin' or 'max_min_improvement'."
            )

        # Constraints
        constraints = []

        # 1. Budget balance: sum_i A[i,j] = 1 for each j
        for j in range(n):
            def _bb_con(x: np.ndarray, j: int = j) -> float:
                A = unpack(x)
                return float(A[:, j].sum() - 1.0)
            constraints.append({"type": "eq", "fun": _bb_con})

        # 2. Actuarial fairness: lambda_i*b_i = sum_j A[i,j]*lambda_j*b_j
        lam_b = lam * b
        for i in range(n):
            def _af_con(x: np.ndarray, i: int = i) -> float:
                A = unpack(x)
                return float(np.dot(A[i, :], lam_b) - lam_b[i])
            constraints.append({"type": "eq", "fun": _af_con})

        # 3. Capacity: b_i - a_{i,j}*b_j + capacity_slack*b_j >= 0
        for i in range(n):
            for j in range(n):
                def _cap_con(x: np.ndarray, i: int = i, j: int = j) -> float:
                    A = unpack(x)
                    return float(b[i] - A[i, j] * b[j] + capacity_slack * b[j])
                constraints.append({"type": "ineq", "fun": _cap_con})

        # Bounds: 0 <= A[i,j] <= 1
        bounds = [(0.0, 1.0)] * (n * n)

        result = minimize(
            obj_fn,
            x0,
            method="SLSQP",
            bounds=bounds,
            constraints=constraints,
            options={"ftol": fairness_tol, "maxiter": 2000, "disp": False},
        )

        A_opt = unpack(result.x).clip(0.0, 1.0)

        return LinearRiskSharingPool(
            allocation_matrix=A_opt,
            claim_intensities=self._lambda.copy(),
            claim_means=self._b.copy(),
            safety_loadings=self._eta.copy(),
            initial_capital=self._u.copy(),
        )

