@dataclass
class ValidationResult:
    """
    Output from LinearRiskSharingPool.validate_conditions().

    Attributes
    ----------
    budget_balance_ok:
        True if sum_i a_{i,j} = 1 for all j (within tolerance).
    actuarial_fairness_ok:
        True if lambda_i * b_i = sum_j a_{i,j} * lambda_j * b_j for all i
        (within tolerance).
    capacity_ok:
        True if a_{i,j} * b_j <= b_i for all i, j (within tolerance).
    budget_balance_violations:
        Array of (column_sum - 1) for each j. Zero means satisfied.
    actuarial_fairness_violations:
        Array of (lhs - rhs) / (lambda_i * b_i) for each i (relative error).
        Zero means satisfied.
    capacity_violations:
        Maximum violation a_{i,j} * b_j - b_i across all i, j. Zero or
        negative means satisfied.
    all_ok:
        True if all three conditions are satisfied.

    Note
    ----
    This class cannot check the scale family condition — that is a property
    of the claim size distributions, not the allocation matrix. You must
    verify scale family membership separately.
    """

    budget_balance_ok: bool
    actuarial_fairness_ok: bool
    capacity_ok: bool
    budget_balance_violations: np.ndarray
    actuarial_fairness_violations: np.ndarray
    capacity_violations: float

    @property
    def all_ok(self) -> bool:
        """True if every matrix condition is satisfied."""
        return self.budget_balance_ok and self.actuarial_fairness_ok and self.capacity_ok

    def __repr__(self) -> str:
        status = "VALID" if self.all_ok else "INVALID"
        bb = "OK" if self.budget_balance_ok else f"FAIL (max={float(np.max(np.abs(self.budget_balance_violations))):.4f})"
        af = "OK" if self.actuarial_fairness_ok else f"FAIL (max_rel={float(np.max(np.abs(self.actuarial_fairness_violations))):.4f})"
        cap = "OK" if self.capacity_ok else f"FAIL (max={self.capacity_violations:.4f})"
        return (
            f"ValidationResult({status})\n"
            f"  budget_balance:      {bb}\n"
            f"  actuarial_fairness:  {af}\n"
            f"  capacity:            {cap}"
        )


@dataclass
class RuinResult:
    """
    Output from LinearRiskSharingPool.ruin_comparison().

    Attributes
    ----------
    pooled:
        Array of shape (n,). Infinite-time ruin probability for each
        participant under the pool.
    standalone:
        Array of shape (n,). Infinite-time ruin probability for each
        participant running independently (no sharing).
    improvement:
        standalone - pooled, elementwise. Positive values mean pooling
        reduced ruin probability for that participant.
    method:
        'cramerlundberg' for exact computation (exponential severity only)
        or 'simulation' for Monte Carlo estimate.
    """

    pooled: np.ndarray
    standalone: np.ndarray
    improvement: np.ndarray
    method: str
    initial_capital: np.ndarray

    def __repr__(self) -> str:
        improved = int(np.sum(self.improvement > 1e-10))
        n = len(self.pooled)
        return (
            f"RuinResult(method={self.method!r}, n={n}, "
            f"improved={improved}/{n}, "
            f"mean_improvement={float(np.mean(self.improvement)):.4f})"
        )


